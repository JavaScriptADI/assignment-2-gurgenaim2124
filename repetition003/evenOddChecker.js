
// task 1 : even or odd numbers checker

let input = prompt("Enter a number")
let num = Number(input)

if(num % 2 === 0){
    console.log("that number is even")
}else if(num % 2 === 1){
    console.log("that number is odd")
}else {
    console.log("that answer does not exist")
}

